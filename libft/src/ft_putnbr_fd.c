#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > -2147483648 && n < 2147483647)
		ft_putstr_fd(ft_itoa(n), fd);
	else
		ft_putstr(E_MESS07);
}

#include "../includes/libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			s[i] = '\0';
			i++;
		}
	}
	else
		ft_putstr(E_MESS02);
}
